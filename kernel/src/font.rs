// ---------------------------------------------------------------------------
// Algorithmic variants
// ---------------------------------------------------------------------------
fn glyph_bold(c: u8, buf: &mut [u8; 18]) -> &[u8] {
    let g = base_glyph(c);
    let n = 8usize;
    for row in 0..n { buf[row] = g[row] | (g[row] >> 1); }
    &buf[..n]
}
fn glyph_italic(c: u8, buf: &mut [u8; 18]) -> &[u8] {
    let g = base_glyph(c);
    let n = 8usize;
    for row in 0..n {
        let shift = (row as u32) / 2;
        buf[row] = (g[row] >> shift) & !(0xFFu8 >> shift);
    }
    &buf[..n]
}
fn glyph_serif(c: u8, buf: &mut [u8; 18]) -> &[u8] {
    let g = base_glyph(c);
    let n = 8usize;
    for row in 0..n { buf[row] = g[row]; }
    match c {
        b'I'|b'J'|b'H'|b'E'|b'F'|b'L'|b'0'..=b'9' => { buf[0] |= 0b10000001; buf[7] |= 0b10000001; }
        _ => {}
    }
    &buf[..n]
}
fn glyph_outline(c: u8, buf: &mut [u8; 18]) -> &[u8] {
    let g = base_glyph(c);
    let n = 8usize;
    for row in 0..n { buf[row] = 0; }
    for row in 0..n {
        for col in 0..8i32 {
            let bit = 0x80u8 >> col;
            if g[row as usize] & bit == 0 { continue; }
            let left = col > 0 && (g[row as usize] & (0x80 >> (col-1))) != 0;
            let right = col < 7 && (g[row as usize] & (0x80 >> (col+1))) != 0;
            let up = row > 0 && (g[(row-1) as usize] & bit) != 0;
            let down = row < 7 && (g[(row+1) as usize] & bit) != 0;
            if !(left && right && up && down) { buf[row as usize] |= bit; }
        }
    }
    &buf[..n]
}

