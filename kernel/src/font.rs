//! Multi-font system: 8 built-in bitmap fonts + algorithmic variants.
//! Zero-Alloc: embedded data + static compute buffers.

#[derive(Clone, Copy, PartialEq)]
pub enum FontId {
    Compact, // 0: 8x8 compact (default)
    Bold,    // 1: 8x8 bold
    Italic,  // 2: 8x8 italic
    Serif,   // 3: 8x8 serif
    Outline, // 4: 8x8 hollow
    Tall,    // 5: 8x14 tall
    Vga,     // 6: 8x16 classic VGA
    Wide,    // 7: 10x18 wide
}

pub const FONT_COUNT: usize = 8;

pub fn font_name(id: FontId) -> &'static [u8] {
    match id {
        FontId::Compact => b"compact",
        FontId::Bold => b"bold",
        FontId::Italic => b"italic",
        FontId::Serif => b"serif",
        FontId::Outline => b"outline",
        FontId::Tall => b"tall",
        FontId::Vga => b"vga",
        FontId::Wide => b"wide",
    }
}

pub fn font_width(id: FontId) -> u32 { match id { FontId::Wide => 10, _ => 8 } }
pub fn font_height(id: FontId) -> u32 {
    match id {
        FontId::Compact | FontId::Bold | FontId::Italic | FontId::Serif | FontId::Outline => 8,
        FontId::Tall => 14,
        FontId::Vga => 16,
        FontId::Wide => 18,
    }
}
pub fn font_advance(id: FontId) -> u32 { font_width(id) }

fn from_id(id: FontId) -> usize { id as usize }

// Per-font glyph buffer + a `&[u8]` ptr for the last lookup
