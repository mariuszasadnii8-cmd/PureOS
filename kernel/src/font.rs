// ---------------------------------------------------------------------------
// 10x18 wide font (scaled from 8x8)
// ---------------------------------------------------------------------------
fn glyph_wide(c: u8, buf: &mut [u8; 18]) -> &[u8] {
    let g = base_glyph(c);
    for row in 0..8 {
        let src = g[row];
        let mut wide = 0u16;
        for col in 0..8 {
            let bit = (src >> (7-col)) & 1;
            let dc = if col >= 4 { col + 1 } else { col };
            wide |= (bit as u16) << (9 - dc);
        }
        let hi = (wide >> 8) as u8 & 0xFF;
        let lo = wide as u8;
        buf[row*2] = hi;
        buf[row*2+1] = lo;
    }
    for i in 16..18 { buf[i] = 0; }
    &buf[..18]
}

/// Test if a specific pixel (row, col) in a glyph is set.
pub fn glyph_pixel(font: FontId, c: u8, row: usize, col: u32) -> bool {
    let g = glyph(font, c);
    let w = font_width(font);
    if w == 10 {
        if row * 2 + 1 >= g.len() { return false; }
        let hi = g[row * 2] as u16;
        let lo = g[row * 2 + 1] as u16;
        let pair = (hi << 8) | lo;
        pair & (0x200u16 >> col) != 0
    } else {
        if row >= g.len() { return false; }
        g[row] & (0x80u8 >> col) != 0
    }
}

